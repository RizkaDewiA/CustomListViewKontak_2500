package com.pam.customlistviewkontak_2500;

public class Kontak {
    private String nama,no;

    public Kontak(String nama, String no) {
        this.nama = nama;
        this.no = no;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
